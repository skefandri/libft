#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		size;
	char	*ptr;

	i = 0;
	size = 0;
	if (!s || !f)
		return (0);
	while (s[size])
		size++;
	ptr = malloc(sizeof(char) * size + 1);
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
